
package Chap05_UsingLoops;

/* Program displays numbers from 1 to 10 on the console */

public class CountToTen {

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {             //for loop with a intialisation, tester and counter expression
            System.out.println(i);                  //integers displayed on console
        }
    }
}
